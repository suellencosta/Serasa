Cypress.Commands.add('postRequest', (path, body) => {
    return cy.request({
        method: 'POST',
        url: path,
        qs: body
      })
});

Cypress.Commands.add('deleteRequest', (path, body) => {
    return cy.request({
      method: 'DELETE',
      url: path,
      qs: body
    })
});  
